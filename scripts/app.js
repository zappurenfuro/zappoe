function filterItems(league) {
  const items = document.querySelectorAll('.item-card');
  items.forEach(item => {
      const itemData = item.dataset;
      if (league === 'settlers' && itemData.character === 'DreamingOnIceNova') {
          item.style.display = 'flex';
      } else if (league === 'necropolis' && itemData.character !== 'DreamingOnIceNova') {
          item.style.display = 'flex';
      } else {
          item.style.display = 'none';
      }
  });
}

